import Foundation
import UIKit

extension UIColor {
    
    class func vocabyRed() -> UIColor {
        return UIColor(red:0.94, green:0.33, blue:0.31, alpha:1.0)
    }
    
    class func vocabyDarkRed() -> UIColor {
        return UIColor(red:0.78, green:0.16, blue:0.16, alpha:1.0)
    }
    
    class func vocabyLightRed() -> UIColor {
        return UIColor(red:0.90, green:0.45, blue:0.45, alpha:1.0)
    }
    
    class func vocabyBackground() -> UIColor {
        return UIColor(red:0.93, green:0.93, blue:0.93, alpha:1.0)
    }
}
